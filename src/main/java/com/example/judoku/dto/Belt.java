public class Belt {
	
	private String Belt;
	private long userId;
	
	
	
	public Belt() {
		super();
	}

	public String getBelt() {
		return Belt;
	}

	public void setBelt(String belt) {
		Belt = belt;
	}

	public long getUserId() {
		return userId;
	}

	public void setUserId(long userId) {
		this.userId = userId;
	}
	
	
	

}
