package com.example.judoku.dto;

public class SilverDto {

	private long userId;
	private String name;
	
	
	
	
	public SilverDto() {
		super();
	}
	public long getUserId() {
		return userId;
	}
	public void setUserId(long userId) {
		this.userId = userId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
}
